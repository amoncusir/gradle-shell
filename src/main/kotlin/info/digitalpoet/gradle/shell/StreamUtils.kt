fun InputStream.getBufferedString(charset: Charset = Charset.defaultCharset(), sBuf: Int = 1024): String
{
    val bufString: StringBuffer = StringBuffer()

    if (available() != 0)
    {
        val buffer = ByteArray(sBuf)
        while (read(buffer) != -1) bufString.append(buffer.toString(charset))
    }

    return bufString.toString()
}

